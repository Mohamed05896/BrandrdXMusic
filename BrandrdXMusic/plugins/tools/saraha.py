TRIGGERS = ["صراحة", "صراحه", "كت", "كات"]

@app.on_message(filters.group & ~filters.bot)
async def boda_pro_questions(client, message):
    if not message.text: 
        return
    
    # التعرف الذكي على الأمر (حتى لو فيه إيموجي أو علامات استفهام)
    text = message.text.strip().lower()
    # استخراج أول كلمة وتنظيفها من الرموز
    clean_command = re.sub(r'[^\w\s]', '', text).split()
    if not clean_command: 
        return
    
    if clean_command[0] in TRIGGERS:
        # المولد يختار سؤال عشوائي وصحيح من القائمة
        question = random.choice(QUESTIONS_BODA)
        # الرد بالسؤال "صافي" بدون مقدمات أو عدادات
        await message.reply_text(f"- {question}")
