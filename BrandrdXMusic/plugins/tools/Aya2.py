# الكلمات المشغلة
QURAN_TRIGGERS = ["اية", "آية", "ايه", "إيه", "قرآنية", "راحه", "وجع دماغ", "زهقان", "زهقانه"]

@app.on_message(filters.text & ~filters.bot, group=8)
async def quran_boda_extended(client, message: Message):
    text = message.text.strip().lower()
    
    if text in QURAN_TRIGGERS:
        verse = random.choice(QURAN_VERSES)
        await message.reply_text(verse)
