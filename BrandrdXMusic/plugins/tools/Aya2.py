# الكلمات اللي بتشغل البوت
QURAN_TRIGGERS = ["ايه", "آية", "قرآن", "قرأن", "آيه", "رحمة ربنا", "آيه قرانيه", "ايه قرانيه"]

@app.on_message(filters.text & filters.group & ~filters.bot)
async def quran_boda_bot(client, message):
    text = message.text.strip()
    if text in QURAN_TRIGGERS:
        verse = random.choice(QURAN_VERSES)
        await message.reply_text(
            f"{verse}\n\n➻ sᴏᴜʀᴄᴇ : بُودَا | ʙᴏᴅᴀ"
        )
